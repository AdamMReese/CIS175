package com.videogamedetails;

// This class contains methods to process the details of two different video games
public class VideoGameDetails {

	// Method to process details of the first video game
	public String processGame1Details(String title, String genre) {
		return "Game 1 Details: " + "Title - " + title + ", Genre - " + genre;
	}

	// Method to process details of the second video game
	public String processGame2Details(String title, String platform) {
		return "Game 2 Details: " + "Title - " + title + ", Platform - " + platform;
	}
}
